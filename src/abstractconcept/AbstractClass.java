package abstractconcept;

public abstract class AbstractClass {

	public abstract void loan();
	
	/*
	 * abstract class contain abstract and non abstract methods
	 * abstract method doesnt have implementation
	 * abstract class is implementing partial abstraction only 
	 * abstraction --> Hiding the implementation
	 * 
	 * Interface should archieve 100% abstraction ,all methods are doesn't have implementation.
	 * 
	 * 
	 */
	
	
	public void credit()
	{
		System.out.println("credit system");
	}
	
	public void debit()
	{
		System.out.println("debit system");
	}
	
	
	
}
